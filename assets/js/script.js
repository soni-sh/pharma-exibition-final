// Change navbar background on scroll
window.addEventListener("scroll", function() {
    let navbar = document.querySelector(".navbar");
    if (window.scrollY > 50) {
        navbar.classList.add("solid");
    } else {
        navbar.classList.remove("solid");
    }
});


